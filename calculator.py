x = input("Enter a Number:")
y = input("Enter Another Number:")
x = int(x)
y = int(y)
sum = x + y
print(f"{x} + {y} = {sum}")

difference = x - y
print(f"{x} - {y} = {difference}")

quotient = x / y
print(f"{x} / {y} = {quotient}")

product = x * y
print(f"{x} * {y} = {product}")

remainder = x % y
print(f"{x} % {y} = {remainder}")

exponentiation = x ** y
print(f"{x} ^ {y} = {exponentiation}")

negation = x *(-1)
print(f"-{x} = {negation}")

negation = y *(-1)
print(f"-{y} = {negation}")

